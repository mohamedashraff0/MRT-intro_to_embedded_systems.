#include <stdio.h>
#include <stdlib.h>

void main(void)
{
   int x=5;
   int y=2;

   int sum;
   sum=x+y;
   printf("sum of two numbers = %d\n",sum);

   int difference;
   difference=x-y;
   printf("difference of two numbers = %d\n",difference);

   int product;
   product=x*y;
   printf("product of two numbers = %d\n", product);

   float quotient;
   quotient=x/y;
   printf("quotient of two numbers = %f\n", quotient);

   int remainder;
   remainder=x%y;
   printf("remainder of two numbers = %d\n", remainder);


}

